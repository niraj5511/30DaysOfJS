let b = 4.16

// Rounding off value to nearest one
console.log(Math.round(b)) // 4

// Rounding off to floor value
console.log(Math.floor(b)) // 4

// Rounding off to cieling value
console.log(Math.ceil(b)) // 5

// Finding minimum
console.log(Math.min(-2,1,5,7))  // Returns -2 as it is minimum

// Finding maximum
console.log(Math.max(-2,1,5,7))  // Returns 7 as it is maximum

// Finding absolute value
console.log(Math.abs(-6))  // Returnd 6

// Finding square root
console.log(Math.sqrt(4))  // 16

// Finding exponential value
console.log(Math.pow(3,2))  // 9

//Trigonometry
console.log(Math.sin(0))  // 0
console.log(Math.cos(0))  // 1