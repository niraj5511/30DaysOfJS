// String to Int
let num2 = '10'
let numInt = parseInt(num2)
console.log(numInt)  //  10

let numInt1 = Number(num2)
console.log(numInt1)  //  10

let numInt2 = +num2
console.log(numInt2)  //  10

// String to Float
let num3 = "10.12"
let numFloat = parseFloat(num3)
console.log(numFloat)  //  10.12
// Other methods of converting into Int are similar for Float

// Float to Int
let floatInt = parseInt(num3)
console.log(floatInt)  //  10