// Pre-Increment
let count = 0
console.log(++count)        // 1
console.log(count)          // 1

// Post-Increment
let num = 0
console.log(num++)  //  0
console.log(num)  //  1

// Pre-Decrement
let num1 = 0
console.log(--num1)  //  -1
console.log(num1)  //  -1

// Post-Decrement
let num2 = 0
console.log(num2--)  //  0
console.log(num2)  // -1